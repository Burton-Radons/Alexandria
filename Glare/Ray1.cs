		/// <summary>A Vector2f position with a Vector2f normal.</summary>
	public struct Ray2f
	{
		/// <summary>Get or set the origin of the <see cref="Ray2f"/>.</summary>
		public Vector2f Origin;

		/// <summary>Get or set the direction of the <see cref="Ray2f"/>.</summary>
		public Vector2f Normal;
		
		public Ray2f(Vector2f origin, Vector2f normal)
		{
			Origin = origin;
			Normal = normal;
		}
	}
